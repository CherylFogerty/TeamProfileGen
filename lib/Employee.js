class Employee {
    constructor (name, id, email){
        this.name = name;
        this.id = id;
        this.email = email;
    }
}

getName() 
    let name = name ();
    return name.getName() - this.name;

getId()
    let id = id ();
    return id.getId() - this.id;

getEmail()
    let email = email ();
    return email.get.Email() - this.email;
